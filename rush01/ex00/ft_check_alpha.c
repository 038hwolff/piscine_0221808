int		ft_puterror(void);

int		ft_check_alpha(char *argv)
{
	int i;

	i = 1;
	while (argv[i])
	{
		if (!(argv[i] >= '1' && argv[i] <= '9') && !(argv[i] == 46))
		{
			ft_puterror();
			return (0);
		}
		i++;
	}
	return (1);
}
