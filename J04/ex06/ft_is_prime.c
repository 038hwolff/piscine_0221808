int		ft_is_prime(int nb)
{
	int	i;

	i = 1;
	if (nb <= 1)
		return (0);
	while (nb / i >= 1)
	{
		i++;
		if (nb % i == 0 && nb > i)
			return (0);
	}
	return (1);
}
